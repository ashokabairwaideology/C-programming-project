<h3><u>Help Reference</u></h3>

<p><b>How do I delete a file?</b></p>
<p>Click on the <input type=checkbox> next to one or more files that you wish to delete. After that, click the delete button at the bottom of your file list.</p>

<p><b>How do I edit a file?</b></p>
<p>Click on the <img src=images/edit.gif> next to the page that you wish to edit. It will take you to a page to edit your page.</p>

<p><b>Do I need to know coding languages to create a website?</b></p>
<p>No you don't.  I have provided a WYSIWYG editor that you can use to design your website.  If you choose, you can design your page in HTML view by either disabling JavaScript on this page, or by viewing the HTML source code from the editor.</p>

<p><b>How do I rename a file?</b></p>
<p>Click on the <img src=images/rename.gif>.  When you do that, you will see a text field appear with the current name of the file.  Change the name of the file to your desired name and click the Go! button.</p>

<p><b>Why can't I use spaces in file and directory names?</b></p>
<p>This is because some servers do not recognize files with spaces in their names.  Therefore, spaces cannot be used.</p>

<p><a name=new_file><b>Creating A New File</b></a></p>
<p>You can name a new file whatever you want, as long as it has no spaces in the name and it ends with a web page extension such as .htm, .html, .shtml, .txt, or .php.  If you have a page that you want people to use to contact you, you can name it contact.php or contact.html or whatever you want.</p>

<p><a name=upload><b>Uploading A File</b></a></p>
<p>To upload a file, click on the browse button in the upload area.  Find the file on your local computer, select it, and then click open.  After that, click the 'upload' button.</p>

<p><a name=delete><b>Removing A File</b></a></p>
<p>To delete a file or multiple files, click on the check box next to each file that you would like to delete.  Once you are finished, click the delete button at the bottom of your file list.</p>

<p><a name=list><b>What is my file list?</b></a></p>
<p>Your file list is a list of files that you have in your website.</p>

